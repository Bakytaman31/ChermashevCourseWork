package com.example.alaarcha;

public class Room {
    private String name;
    private String hotel;
    private int price;
    private int image;

    public Room(String name, String hotel, int price,int image){
        this.name = name;
        this.hotel = hotel;
        this.price = price;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHotel() {
        return hotel;
    }

    public void setHotel(String hotel) {
        this.hotel = hotel;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
